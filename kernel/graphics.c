static void copy_to_frontbuffer(void) {
    for (uint32_t y = 0; y < gfx.height; ++y) {
        memcpy(&gfx.frontbuffer[y * gfx.pitch_pixels],
               &gfx.backbuffer[y * gfx.width],
               gfx.width * sizeof(uint32_t));
    }
}

void graphics_init(const boot_info_t *boot_info) {
    memset(&gfx, 0, sizeof(gfx));
    if (!boot_info || boot_info->framebuffer_addr == 0 || boot_info->framebuffer_bpp != 32) {
        return;
    }

    gfx.width = boot_info->framebuffer_width;
    gfx.height = boot_info->framebuffer_height;
    gfx.pitch_pixels = boot_info->framebuffer_pitch / 4;
    gfx.bpp = boot_info->framebuffer_bpp;
    gfx.frontbuffer = (uint32_t *)(uintptr_t)boot_info->framebuffer_addr;
    gfx.backbuffer = kmalloc((size_t)gfx.width * gfx.height * sizeof(uint32_t));
    if (!gfx.backbuffer) {
        memset(&gfx, 0, sizeof(gfx));
        return;
    }

    detect_gpu_via_pci();
    gfx.ready = true;
    graphics_clear(0x00000000);
    graphics_present();
}

bool graphics_available(void) {
    return gfx.ready;
}

uint32_t graphics_width(void) {
    return gfx.width;
}

uint32_t graphics_height(void) {
    return gfx.height;
}

void graphics_clear(gfx_color_t color) {
    if (!gfx.ready) {
        return;
    }
    for (uint32_t i = 0; i < gfx.width * gfx.height; ++i) {
        gfx.backbuffer[i] = color;
    }
}

void draw_pixel(int x, int y, gfx_color_t color) {
    if (!gfx.ready || x < 0 || y < 0 || (uint32_t)x >= gfx.width || (uint32_t)y >= gfx.height) {
        return;
    }
    gfx.backbuffer[(uint32_t)y * gfx.width + (uint32_t)x] = color;
}

void draw_rect(int x, int y, int width, int height, gfx_color_t color) {
    if (!gfx.ready || width <= 0 || height <= 0) {
        return;
    }
    for (int yy = 0; yy < height; ++yy) {
        for (int xx = 0; xx < width; ++xx) {
            draw_pixel(x + xx, y + yy, color);
        }
    }
}

void draw_border(int x, int y, int width, int height, gfx_color_t color) {
    if (!gfx.ready || width <= 1 || height <= 1) {
        return;
    }
    draw_rect(x, y, width, 1, color);
    draw_rect(x, y + height - 1, width, 1, color);
    draw_rect(x, y, 1, height, color);
    draw_rect(x + width - 1, y, 1, height, color);
}

void draw_rect_vertical_gradient(int x, int y, int width, int height, gfx_color_t top, gfx_color_t bottom) {
    if (!gfx.ready || width <= 0 || height <= 0) {
        return;
    }

    for (int yy = 0; yy < height; ++yy) {
        uint32_t t = (uint32_t)yy * 255u / (uint32_t)(height > 1 ? (height - 1) : 1);
        uint8_t r = (uint8_t)((color_r(top) * (255u - t) + color_r(bottom) * t) / 255u);
        uint8_t g = (uint8_t)((color_g(top) * (255u - t) + color_g(bottom) * t) / 255u);
        uint8_t b = (uint8_t)((color_b(top) * (255u - t) + color_b(bottom) * t) / 255u);
        draw_rect(x, y + yy, width, 1, rgb(r, g, b));
    }
}

static void draw_char_scaled(int x, int y, unsigned char c, gfx_color_t color, int scale) {
    if (c >= ARRAY_SIZE(font8x8_basic)) {
        c = '?';
    }
    if (scale < 1) {
        scale = 1;
    }
    for (int row = 0; row < 8; ++row) {
        uint8_t bits = font8x8_basic[c][row];
        for (int col = 0; col < 8; ++col) {
            if (bits & (1u << col)) {
                draw_rect(x + col * scale, y + row * scale, scale, scale, color);
            }
        }
    }
}

void draw_text(int x, int y, const char *text, gfx_color_t color) {
    draw_text_scaled(x, y, text, color, 1);
}

void draw_text_scaled(int x, int y, const char *text, gfx_color_t color, int scale) {
    if (!gfx.ready || !text) {
        return;
    }
    int cursor_x = x;
    while (*text) {
        draw_char_scaled(cursor_x, y, (unsigned char)*text++, color, scale);
        cursor_x += 8 * scale;
    }
}

void graphics_present(void) {
    if (!gfx.ready) {
        return;
    }
    copy_to_frontbuffer();
}
const char* graphics_get_gpu_vendor(void) {
    return gfx.gpu_vendor[0] ? gfx.gpu_vendor : "Unknown";
}

const char* graphics_get_gpu_model(void) {
    return gfx.gpu_model[0] ? gfx.gpu_model : "Unknown";
}

// Simple PCI vendor/device lookup
static void detect_gpu_via_pci(void) {
    // Common GPU vendor IDs
    strcpy(gfx.gpu_vendor, "VESA/VBE");
    strcpy(gfx.gpu_model, "Framebuffer");
}